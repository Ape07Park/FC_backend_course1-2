package fc00.part2;

public class FourArithmetic {
    public static void main(String[] args){
        int a, b;
        a = 12;
        b = 2;

        System.out.println("a+b ="+(a+b));
        System.out.println("sub ="+(a-b));
        System.out.println("mul =" +(a*b));
        System.out.println("div =" +(a/b));
    }
}
